package com.kavinschool.operators;

public class ArithmeticOperatorsExamples {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		int c = a + b;
		System.out.println("a + b = " + c);
		c = a - b;
		System.out.println("a - b = " + c);
		c = a * b;
		System.out.println("a * b = " + c);
		c = a / b;
		System.out.println("b / a = " + c);
		c = b % a;
		System.out.println("b % a = " + c); // Modulus operator // Remainder

	}

}
